    private void btnLuuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLuuMouseClicked
        // TODO add your handling code here:

        int tableIndex = cboSoBan.getSelectedIndex() + 1;
        ArrayList<MonAn> listFood = new ArrayList<>();
      
        for (int i = 0; i < tbMonAn.getRowCount(); i++) {
            MonAn ma = new MonAn(i, tbMonAn.getValueAt(i, 0).toString(), Float.parseFloat(tbMonAn.getValueAt(i, 1).toString()));
            listFood.add(ma);
        }
        try {
            hdDao.WriteTable("ban" + tableIndex + ".txt", listFood);

        } catch (IOException ex) {
            Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        cboLoaiBan.setSelectedIndex(0);
        btnLuu.setEnabled(false);
        lblTong1.setText("0");
        cboSoBan.setSelectedIndex(0);
        DefaultTableModel model = (DefaultTableModel) tbMonAn.getModel();
        model.setRowCount(0);
        model.setNumRows(0);
    }//GEN-LAST:event_btnLuuMouseClicked


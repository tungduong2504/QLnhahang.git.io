    public void ShowMenu() throws SQLException {
        list.clear();
        Connection conn = connect.getConnect();
        Statement st = null;
        ResultSet rs = null;
        String ten = null;
        float gia = (float) 0.0;
        String sql = "SELECT tenMon, gia FROM `qlmonan` WHERE tenMon='" + txtMonAn.getSelectedItem().toString() + "' LIMIT 1";
        st = conn.createStatement();
        rs = st.executeQuery(sql);

        while (rs.next()) {
            MonAn f = new MonAn();
            f.setTenMon((rs.getString("tenMon")));
            f.setGia((rs.getFloat("gia")));
            ten = rs.getString("tenMOn");
            gia = rs.getFloat("gia");
            list.add(f);
        }
        DefaultTableModel model = (DefaultTableModel) tbMonAn.getModel();
        model.addRow(new Object[]{ten, gia});
    }


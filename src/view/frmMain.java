    public ArrayList<MonAn> ShowTenMonAn() throws SQLException {
        list.clear();
        Connection conn = connect.getConnect();
        Statement st = null;
        ResultSet rs = null;

        String sql = "select * from qlmonan";
        st = conn.createStatement();
        rs = st.executeQuery(sql);
        frmMain m = new frmMain();
        while (rs.next()) {
            MonAn f = new MonAn();
            f.setIdMonAn(rs.getInt("idMon"));
            f.setTenMon((rs.getString("tenMon")));
            f.setGia((rs.getFloat("gia")));
            txtMonAn.addItem(rs.getString("tenMon"));
            list.add(f);
        }
        return list;
    }


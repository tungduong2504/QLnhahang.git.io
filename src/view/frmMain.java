    public void calMoney() {
        float columCount = tbMonAn.getRowCount();
        float sum = 0;
        for (int i = 0; i < columCount; i++) {
            sum += Float.parseFloat(tbMonAn.getValueAt(i, 1) + "");
        }
        lblTong1.setText(sum + "");
    }
